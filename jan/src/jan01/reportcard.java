package jan01;

public class reportcard {
	public static void main(String[] args) {
		int marks = 90;
		if(marks>=90) {
			System.out.println("A");
		}else if(marks>=80) {
			System.out.println("b");
		}else if(marks>=70) {
			System.out.println("c");
	}else if(marks>=60) {
		System.out.println("d");
	}else if(marks<=50) {
		System.out.println("f");
	}

}
}
