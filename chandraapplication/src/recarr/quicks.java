package recarr;

public class quicks {
	public static void main(String[] args) {
		int[] arr = {10,20,30,40,50};
		
	}
	public static void quick(int arr , int s, int e) {
		int mid = (s+e)/2;
		int pivot = arr[mid];
		int l = s;
		int r = e;
		while(true) {
			while(l<=r) {
				l++;
			}
			while(r<=l) {
				r++;
			}
		}
	}

}
