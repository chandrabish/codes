	public static boolean isPalin(String str) {
		 int idx1=0;
		int idx2= str.length()-1;
		while(true) {
			if(str.charAt(idx1)!= str.charAt(idx2));
			return false;
			
					
		}
		idx1++;
		idx2--;
		
	}
